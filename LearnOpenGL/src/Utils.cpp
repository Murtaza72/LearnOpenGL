	GLCall(glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(6 * sizeof(float))));
	GLCall(glBindVertexArray(0));

	GLCall(glBindVertexArray(planeVAO));
	GLCall(glDrawArrays(GL_TRIANGLES, 0, 6));
